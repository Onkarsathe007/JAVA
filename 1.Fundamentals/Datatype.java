// package Fundamentals;
class DataType 
{
    public static void main(String[] args) {
      // Integer (int) - whole numbers
      int age = 30;
  
      // Double (double) - floating-point numbers with decimals
      double pi = 3.14159;
  
      // Character (char) - single character
      char initial = 'J';
  
      // Boolean (boolean) - true or false values
      boolean isSunny = true;
  
      // Byte (byte) - small whole numbers (8 bits)
      byte b = 127; // Can only store values between -128 and 127
  
      // Short (short) - whole numbers (16 bits)
      short temperature = 37; // Can store values between -32768 and 32767
  
      // Long (long) - large whole numbers (64 bits)
      long worldPopulation = 8000000000L; // Use L suffix for long values
  
      System.out.println("Age: " + age);
      System.out.println("Pi: " + pi);
      System.out.println("Initial: " + initial);
      System.out.println("Is Sunny: " + isSunny);
      System.out.println("Byte value: " + b);
      System.out.println("Temperature: " + temperature);
      System.out.println("World Population: " + worldPopulation);
    }
  }
  